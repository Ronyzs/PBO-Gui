    private void tabelDBMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelDBMouseClicked
        PengisianData a = new PengisianData();
        int row = tabelDB.rowAtPoint(evt.getPoint());
        String Nama = tabelDB.getValueAt(row, 0).toString();
        a.txtNama.setText(Nama);//ama
        String NIM = tabelDB.getValueAt(row, 1).toString();
        a.txtNIM.setText(NIM);//nim
        String alm = tabelDB.getValueAt(row, 5).toString();
        a.txtAlamat.setText(alm);
        String jkm = "Laki - laki";
        if (jkm.equals(tabelDB.getValueAt(row, 2))) {//male
            a.rdMale.doClick();
            a.rdFemale.setSelected(false);
        }
        String jkf = "Perempuan";
        if (jkf.equals(tabelDB.getValueAt(row, 2))) {//female
            a.rdFemale.doClick();
            a.rdMale.setSelected(false);
        }
        
        a.cbJurusan.setSelectedItem(tabelDB.getValueAt(row, 3)); // jurusan
        
        String date = tabelDB.getValueAt(row, 4).toString();
        Date date2 = null;
        try {
            date2 = new SimpleDateFormat("dd/MM/yyyy").parse(date);
        } catch (ParseException ex) {
            Logger.getLogger(tabelData.class.getName()).log(Level.SEVERE, null, ex);
        }
        a.tanggalLahir.setDate(date2);//tanggal lahir
        
        
        a.setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_tabelDBMouseClicked

